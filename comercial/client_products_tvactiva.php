    <script language="JavaScript">
        $(document).ready(function () {

            // Fold-Unfold ITEMS
            // We start closing ALL (on load PAGE) not marked has "opened"
            $('.services-selector:not(".opened")').children('.services-full-info').hide();

            // Then we configure actions for fold-unfold
            $('.services-element').click(function () {
                var yoSoyTuPadre = $(this).parent('.services-selector');
                yoSoyTuPadre.toggleClass('opened');
                yoSoyTuPadre.children('.services-full-info').slideToggle();
            });

            // ALERTS-ColorBoxes
            var floatingWrapper = $('.general-alert-wrapper'),
                floatingAlert = $('.content-general-wrapper.clientproducts'),
                alertTitle = $('.alert-general-title'),
                alertBody = $('.alert-general-body'),
                scrollTop = $(window).scrollTop();

            // Change PASS
            $('a.change-pass').click(function (cp) {
                cp.preventDefault();

                // Call FORM change PASSWORD
                $.post("includes/fake_api.php", {operation: 'changepass'})
                    .done(function (data) {
                        alertTitle.html(data.title);
                        alertBody.html(data.body);
                    })
                    .fail(function (data) {
                        alertTitle.html(data);
                        alertBody.html(data);
                    });
                if ($(window).width() < 480) {
                    floatingWrapper.removeClass('fixedone');
                    floatingAlert.css({top: scrollTop});
                }
                revealalert();

                // Inner navigation
                $('.clientproducts').on('click', '.cp-trigger', function () {
                    // change PASS confirmation
                    $.post("includes/fake_api.php", {operation: 'confirm_cp'})
                        .done(function (data) {
                            alertTitle.html(data.title);
                            alertBody.html(data.body);
                        })
                        .fail(function (data) {
                            alertTitle.html('Error...');
                            alertBody.html('We can\'t process your request now...');
                        });
                    setTimeout(function () {
                        closeAlert();
                    }, 5000);
                });
            });


            // View Channels
            $('a.view-channels').click(function (vc) {
                vc.preventDefault();


                // Call FORM change PASSWORD
                $.post("includes/fake_api.php", {operation: 'viewchannels'})
                    .done(function (data) {
                        alertTitle.html(data.title);
                        alertBody.html(data.body);
                    })
                    .fail(function (data) {
                        alertTitle.html(data);
                        alertBody.html(data);
                    });
                if ($(window).width() < 480) {
                    floatingWrapper.removeClass('fixedone');
                    floatingAlert.css({top: scrollTop});
                }
                revealalert();
            });

            // Close Alert
            $('.clientproducts').on('click', '.close-button', function () {
                closeAlert();
            });


        });
    </script>

